# 2-arg: avoid overwriting utils/chunks.jl's tochunk(Any, Any) and tochunk(Any); only add Processor/MemorySpace variants
# Chunk + Processor: disambiguate vs utils/chunks.jl's tochunk(x::Chunk, proc; ...)
tochunk(x::Chunk, proc::Processor; kwargs...) = tochunk(x, proc, AnyScope(); kwargs...)
tochunk(x, proc::Processor; kwargs...) = tochunk(x, proc, AnyScope(); kwargs...)
tochunk(x, space::MemorySpace; kwargs...) = tochunk(x, space, AnyScope(); kwargs...)

# 1-arg: route through the current acceleration so the resulting Chunk gets
# the appropriate handle and memory space (e.g. MPIRef + MPIMemorySpace under
# MPIAcceleration, instead of a rank-local DRef + CPURAMMemorySpace).
# N.B. This overrides the default-argument method generated by
# utils/chunks.jl's `tochunk(x, proc=OSProc(), scope=AnyScope())`.
function tochunk(x)
    accel = current_acceleration()
    return tochunk(x, default_processor(accel, x), default_memory_space(accel, x), AnyScope())
end

check_proc_space(x, proc, space) = nothing
function check_proc_space(x::Chunk, proc, space)
    if x.space !== space
        throw(ArgumentError("Memory space mismatch: Chunk=$(x.space) != Requested=$space"))
    end
end
